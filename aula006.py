# Tipos primitivos e saídas de dados

'''4 tipos primitivos:
    str: string - texto ou caracteres
    int: números inteiros
    float: números reais - números de ponto flutuante
    bool: valores lógicos ou booleanos = True - False
    '''

a = input('digite algo: ')
print(a.isalpha())
print(a.isnumeric())
print(a.isidentifier())
print(a.isupper())
print(a.isprintable())
